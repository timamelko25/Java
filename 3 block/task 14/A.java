public class A {
    public final int a;  // Объявление переменной как final

    // Конструктор с параметром для инициализации переменной a
    public A(int value) {
        a = value;  // Инициализация final переменной
    }

    // Конструктор по умолчанию
    public A() {
        a = 10;  // Инициализация final переменной с значением по умолчанию
    }

    public static void main(String[] args) {
        A obj1 = new A(5);  // Использование конструктора с аргументом для инициализации a
        System.out.println("Значение a (через конструктор с аргументом): " + obj1.a);

        A obj2 = new A();  // Использование конструктора по умолчанию для инициализации a
        System.out.println("Значение a (через конструктор по умолчанию): " + obj2.a);
    }
}
